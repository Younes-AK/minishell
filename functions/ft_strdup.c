#include "../minishell.h"

char	*ft_strdup(const char *str)
{
	char	*ptr;
	int		i;	

	i = 0;
	if (!str)
		return (NULL);
	ptr = malloc(sizeof(char) * ft_strlen(str) + 1);
	if (ptr == NULL)
		return (NULL);
	while (str[i])
	{
		ptr[i] = str[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
