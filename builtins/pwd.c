#include "../minishell.h"

extern int	g_exit_status;

void	pwd(void)
{
	char	cwd[PATH_MAX];

	getcwd(cwd, PATH_MAX);
	ft_putstr_fd(cwd, 1);
	ft_putstr_fd("\n", 1);
	g_exit_status = 0;
}
